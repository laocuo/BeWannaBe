package com.jumpjump;

public class SingleThread {

    private static SingleThread instance;

    private SingleThread()
    {

    }

    synchronized public static SingleThread getInstance()
    {
        if (instance == null)
        {
            return new SingleThread();
        }
        else
        {
            return instance;
        }
    }
}
